// ------------------------------------------------------------
// 4. 운행 날짜와 시간
// ------------------------------------------------------------
// 아래만 바꾸면 운행 날짜와 시간이 바뀝니다.
//
// daysFromTo(5, 9)   = 5일부터 9일까지
// daysFromTo(10, 20) = 10일부터 20일까지
//
// departures 안의 뜻:
// time: 출발 시간
// routes: 운행하는 노선 번호
// ------------------------------------------------------------

// 5일부터 9일까지처럼 연속 날짜를 쉽게 만들기 위한 도우미입니다.
function daysFromTo(startDay, endDay) {
  const days = [];

  for (let day = startDay; day <= endDay; day += 1) {
    days.push(day);
  }

  return days;
}

// 날짜를 "2026-06-05"처럼 자동으로 만들어주는 도우미입니다.
function makeDate(day) {
  return `${busInfo.serviceYear}-${String(busInfo.serviceMonth).padStart(2, "0")}-${String(day).padStart(2, "0")}`;
}

